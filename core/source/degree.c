Degree__u9 get_angle_between__vectors_3i32(
        Vector__3i32 source__3i32, 
        Vector__3i32 target__3i32) {

    // step 1) find cartesian quadrant

    i32 d_x = 
        target__3i32.x__i32
        - source__3i32.x__i32
        ;
    i32 d_y = 
        target__3i32.y__i32
        - source__3i32.y__i32
        ;

    // trivial cases
    if (!d_x && !d_y)
        return 0;
    if (!d_x)
        return 
            (d_y < 0)
            ? ANGLE__180
            : 0
            ;
    if (!d_y)
        return
            (d_x < 0)
            ? ANGLE__270
            : ANGLE__90
            ;

    bool is_reciprocated = 
        abs(d_x) < abs(d_y);
    i32 rise, run;
    if (is_reciprocated) {
        rise = abs(d_x);
        run = abs(d_y);
    } else {
        rise = abs(d_y);
        run = abs(d_x);
    }

    ///
    /// Darius:
    /// StackOverflow user "chux - Reinstate Monica"
    /// helped me figure this out with their posted comment:
    /// https://stackoverflow.com/questions/42537957/fast-accurate-atan-arctan-approximation-algorithm
    ///
    i32 x = (rise<<10) / run;
    i32 in_radians_F10 = 
        ((PI_4*x) >> 10) 
        - (
                (x 
                 * (
                     (abs(x)-(1<<10))
                     * (
                         (0b00011111010 
                          + ((0b0001000110 * abs(x)) >> 10)
                          )) >> 10)
                 ) >> 10)
        ;
    Degree__u9 angle = 
        ((in_radians_F10 * PI_TO_DEGREE) >> 12);

    if (!is_reciprocated) {
        if (((d_x < 0 && d_y < 0) 
                        || (d_x > 0 && d_y > 0))) {
            angle = ANGLE__90 - angle;
        }
    } else {
        if (((d_x > 0 && d_y < 0) 
                        || (d_x < 0 && d_y > 0))) {
            angle = ANGLE__90 - angle;
        }
    }

    if (d_y < 0 && d_x < 0) {
        return angle + ANGLE__180;
    }
    if (d_y < 0) {
        return angle + ANGLE__90;
    }
    if (d_x < 0) {
        return angle + ANGLE__270;
    }

    return angle;
}
